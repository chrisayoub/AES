        key = schedule[round_key_num]

        a3 = (0x000000ff & key)
        a2 = (0x0000ff00 & key) >> 8
        a1 = (0x00ff0000 & key) >> 16
        a0 = (0xff000000 & key) >> 24

        matrix[0][col] = matrix[0][col] ^ a0
        matrix[1][col] = matrix[1][col] ^ a1
        matrix[2][col] = matrix[2][col] ^ a2
        matrix[3][col] = matrix[3][col] ^ a3

        col += 1
        round_key_num += 1
