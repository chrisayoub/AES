# https://stackoverflow.com/questions/1035340/reading-binary-file-and-looping-over-each-byte
def bytes_from_file(filename, chunksize=8192):
    with open(filename, "rb") as f:
        while True:
            chunk = f.read(chunksize)
            if chunk:
                yield from chunk
            else:
                break


def load_keyfile(keyfile):
    global schedule

    val = 0
    count = 0
    for b in bytes_from_file(keyfile):
        val = val << 8 | int(b)

        count += 1
        if count == L:
            schedule.append(val)
            val = 0
            count = 0


def encrypt(keysize, keyfile, inputfile, outputfile):
    global rounds, ks

    out = open(outputfile, 'wb')
    load_keyfile(keyfile)
    ks = keysize

    if keysize == 128:
        rounds = 10
    elif keysize == 256:
        rounds = 14

    matrix = []
    row = 0
    col = 0

    for b in bytes_from_file(inputfile):
        if row == 0 and col == 0:
            matrix = []
            for i in range(L):
                matrix.append([])
        matrix[row].append(b)

        # Move to next spot
        row += 1
        if row == L:
            row = 0
            col += 1
        if col == L:
            col = 0
            # Can now encrypt block!
            encrypt_block(matrix)
            write_block(matrix, out)

    # One last partial block left?
    # Get the padding TODO
    pad = matrix[L - 1][L - 1]
    while col < L:
        while row < L:
            matrix[row].append(0)
            row += 1
        row = 0
        col += 1
    matrix[L - 1][L - 1] = pad
    # Now encrypt this last block
    encrypt_block(matrix)
    write_block(matrix, out)


def write_block(matrix, out):
    result = []

    col = 0
    while col < L:
        row = 0
        while row < L:
            result.append(matrix[row][col])
            row += 1
        col += 1

    out.write(bytes(result))


def decrypt_block(matrix):
    global round_key_num
    round_key_num = 0

    round_key_encrypt(matrix)
    for i in range(rounds - 1):
        sub_bytes_encrypt(matrix)
        shift_rows_encrypt(matrix)
        mix_cols_encrypt(matrix)
        round_key_encrypt(matrix)
    sub_bytes_encrypt(matrix)
    shift_rows_encrypt(matrix)
    round_key_encrypt(matrix)


